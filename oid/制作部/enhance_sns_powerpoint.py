    def add_slide(self, prs, title, items):
        slide = prs.slides.add_slide(prs.slide_layouts[6])
        bg = slide.background.fill
        bg.solid()
        bg.fore_color.rgb = RGBColor(255, 255, 255)

        title_box = slide.shapes.add_textbox(Inches(0.5), Inches(0.3), Inches(9), Inches(0.6))
        tf = title_box.text_frame
        tf.text = title
        p = tf.paragraphs[0]
        p.font.size = Pt(32)
        p.font.bold = True
        p.font.color.rgb = self.color

        y = 1.1
        for item in items:
            box = slide.shapes.add_textbox(Inches(0.7), Inches(y), Inches(8.6), Inches(0.45))
            tf = box.text_frame
            tf.word_wrap = True
            tf.text = item
            p = tf.paragraphs[0]
            p.font.size = Pt(12)
            p.font.color.rgb = RGBColor(44, 62, 80)
            y += 0.5

